"""Kairos Agent — SQLAlchemy ORM Models.

Maps to the database schema defined in Section 10 of the implementation document.
All tables use UUID primary keys and support multi-pipeline operation.
"""

from __future__ import annotations

import uuid
from datetime import datetime

from sqlalchemy import (
    Boolean,
    DateTime,
    ForeignKey,
    Index,
    Integer,
    Numeric,
    String,
    Text,
    func,
)
from sqlalchemy.dialects.postgresql import JSONB, UUID
from sqlalchemy.orm import DeclarativeBase, Mapped, mapped_column, relationship


class Base(DeclarativeBase):
    """SQLAlchemy declarative base for all models."""


class PipelineRun(Base):
    """Top-level pipeline execution record."""

    __tablename__ = "pipeline_runs"

    pipeline_run_id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True), primary_key=True, default=uuid.uuid4
    )
    pipeline: Mapped[str] = mapped_column(String(50), nullable=False)
    idea_id: Mapped[uuid.UUID | None] = mapped_column(UUID(as_uuid=True), nullable=True)
    simulation_id: Mapped[uuid.UUID | None] = mapped_column(UUID(as_uuid=True), nullable=True)
    output_id: Mapped[uuid.UUID | None] = mapped_column(UUID(as_uuid=True), nullable=True)
    status: Mapped[str] = mapped_column(String(50), default="running")
    started_at: Mapped[datetime] = mapped_column(DateTime, server_default=func.now())
    completed_at: Mapped[datetime | None] = mapped_column(DateTime, nullable=True)
    total_cost_usd: Mapped[float | None] = mapped_column(Numeric(8, 4), nullable=True)
    total_duration_sec: Mapped[int | None] = mapped_column(Integer, nullable=True)

    __table_args__ = (
        Index("idx_pipeline_runs_status", "pipeline", "status"),
        Index("idx_pipeline_runs_started", started_at.desc()),
    )

    # Relationships (Finding 2.4)
    ideas: Mapped[list["VideoIdea"]] = relationship(back_populates="pipeline_run", cascade="all, delete-orphan")
    simulations: Mapped[list["Simulation"]] = relationship(back_populates="pipeline_run", cascade="all, delete-orphan")
    outputs: Mapped[list["Output"]] = relationship(back_populates="pipeline_run", cascade="all, delete-orphan")
    agent_runs: Mapped[list["AgentRun"]] = relationship(back_populates="pipeline_run", cascade="all, delete-orphan")


class VideoIdea(Base):
    """Ideas / concepts generated by the Idea Agent."""

    __tablename__ = "video_ideas"

    idea_id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True), primary_key=True, default=uuid.uuid4
    )
    pipeline_run_id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        ForeignKey("pipeline_runs.pipeline_run_id", ondelete="CASCADE"),
        nullable=False,
    )
    pipeline: Mapped[str] = mapped_column(String(50), nullable=False)
    concept: Mapped[dict] = mapped_column(JSONB, nullable=False)  # type: ignore[type-arg]
    category: Mapped[str | None] = mapped_column(String(100), nullable=True)
    novelty_score: Mapped[float | None] = mapped_column(Numeric(3, 1), nullable=True)
    feasibility_score: Mapped[float | None] = mapped_column(Numeric(3, 1), nullable=True)
    status: Mapped[str] = mapped_column(String(50), default="pending")
    performance_data: Mapped[dict | None] = mapped_column(JSONB, nullable=True)  # type: ignore[type-arg]
    created_at: Mapped[datetime] = mapped_column(DateTime, server_default=func.now())

    __table_args__ = (
        Index("idx_ideas_pipeline_status", "pipeline", "status"),
        Index("idx_ideas_category", "pipeline", "category", created_at.desc()),
    )

    pipeline_run: Mapped["PipelineRun"] = relationship(back_populates="ideas")


