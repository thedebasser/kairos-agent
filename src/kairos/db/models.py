class TrainingExample(Base):
    """Training examples for few-shot learning and fine-tuning.

    Populated automatically after successful pipeline runs.
    The ``verified`` flag is the final gate — examples are NOT injected
    into prompts until ``verified=True``.  This lets the system accumulate
    data safely while operators decide when to "go live".
    """

    __tablename__ = "training_examples"

    example_id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True), primary_key=True, default=uuid.uuid4
    )
    simulation_id: Mapped[uuid.UUID] = mapped_column(UUID(as_uuid=True), nullable=False)
    pipeline: Mapped[str] = mapped_column(String(50), nullable=False)
    category: Mapped[str | None] = mapped_column(String(100), nullable=True)
    concept_brief: Mapped[dict] = mapped_column(JSONB, nullable=False)  # type: ignore[type-arg]
    simulation_code: Mapped[str] = mapped_column(Text, nullable=False)
    reasoning: Mapped[str | None] = mapped_column(Text, nullable=True)
    thinking_content: Mapped[str | None] = mapped_column(Text, nullable=True)
    validation_passed: Mapped[bool] = mapped_column(Boolean, nullable=False)
    human_approved: Mapped[bool] = mapped_column(Boolean, nullable=False)
    verified: Mapped[bool] = mapped_column(Boolean, nullable=False, default=False)
    iteration_count: Mapped[int] = mapped_column(Integer, nullable=False, default=1)
    rejection_reason: Mapped[str | None] = mapped_column(String(50), nullable=True)
    created_at: Mapped[datetime] = mapped_column(DateTime, server_default=func.now())

    __table_args__ = (
        Index("idx_training_approved", "pipeline", "human_approved", "validation_passed"),
        Index("idx_training_verified", "pipeline", "category", "verified"),
    )


